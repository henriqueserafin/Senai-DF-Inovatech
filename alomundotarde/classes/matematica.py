class Matematica():
    def __init__(self,x,y):
        self.x = x
        self.y = y
    def somar(self):
        return self.x + self.y
    def subtrair(self):
        return self.x - self.y
    def multiplicar(self):
        return self.x * self.y
    def dividir(self):
        return self.x / self.y
    def potencia(self):
        return self.x ** self.y
    def raiz(self):
        return self.x ** (1/self.y)
    
    